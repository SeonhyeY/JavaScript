// Number
let integar = 123;
let negative = -123;
let double = 1.23;
console.log(integar, negative, double); // 123, -123, 1.23

let binary = 0b1111011; // 2진수
let octal = 0o173; // 8진수
let hex = 0x7b; // 16진수
console.log(binary, octal, hex); // 123, 123, 123

console.log(0 / 123); // 0
console.log(123 / 0); // Infinity
console.log(123 / -0); // -Infinity
console.log(123 / 'a'); // NaN

let bigInt = 123456789012345678901234567890n;
console.log(bigInt); //123456789012345678901234567890n
