/** Variable Rules!
 * Latin characters (0-9, a-z, A-Z), and _ only
 * Case-sensitive
 * Recommended: camelCase (likeThis) ✅
 * Korean characters ❌
 * Reserved keywords (e.g., if, this, trur, false, ...) ❌
 * Cannot start with a number ❌
 * Special characters ❌ (except _ and $)
 * Emojis ❌
 * Avoid using numbers like 1, 2, 3 to distinguish multiple variables -> Use meaningful, specific names instead
 */

